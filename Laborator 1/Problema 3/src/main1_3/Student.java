package main1_3;

public class Student {
	private String STname;
	private int STage;
	private double STmark;
	
	// student
	public Student(String STname, int STage, double STmark) {
		this.STname = STname;
		this.STage = STage;
		this.STmark = STmark;
	}
	
	// getter Student
	public String getSTname() {
		return STname;
	}
	public int getSTage() {
		return STage;
	}
	public double getSTmark() {
		return STmark;
	}
	
	
	
}
